$(function(){
	$('#subject_code').autocomplete({
	    lookup: subjects,
	    onSelect: function (suggestion) {
	    	if(current_subjects.indexOf(suggestion.data)==-1){
	    		current_subjects.push(suggestion.data);
	    		$(this).val("")
		    	$.post("/subject/courses", { subject_code: suggestion.data }, function( data ) {
		    		add_subject(data);
		    	}, "json");	
	    	}
	    }
	});

	$("body").on("click", "button.close", function(event){
		current_subjects.remove(current_subjects.indexOf($(this).attr("data-subject-code")))
		$(this).parent().parent().hide('slow', function(){ $(this).parent().remove(); });
	});	

	$("body").on("hover", '[data-toggle="tooltip"]', function(){
		$(this).tooltip({'placement': 'right'});
	});
});
